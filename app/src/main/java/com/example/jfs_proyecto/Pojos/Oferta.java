package com.example.jfs_proyecto.Pojos;

public class Oferta {
    private String nombre, id;

    public Oferta() {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
